#include "common.hpp"

class NumArray {
public:
    NumArray(vector<int>& nums) {
        int sum = 0;
        for (auto & num : nums) {
            sum += num;
            cumulative_sum.push_back(sum);
        }
    }
    
    int sumRange(int left, int right) {
        return sumRange(right) - sumRange(left - 1);
    }

private:
    vector<int> cumulative_sum;

    int sumRange(int right) {
        if (right == -1) return 0;
        else return cumulative_sum[right];
    }
};

